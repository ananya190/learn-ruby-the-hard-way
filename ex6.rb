# Assigning 10 to variable types_of_people
types_of_people = 10
# Interpolating types_of_people in string variable x
x = "There are #{types_of_people} types of people."
# Assigning string "binary" to variable binary
binary = "binary"
# Assigning string "don't" to variable do_not
do_not = "don't"
# Interpolating variables binary and do_not in string variable y
y = "Those who know #{binary} and those who #{do_not}"

# printing x
puts x
# printing y
puts y

# printing interpolation of x in string
puts "I said: #{x}"
# printing interpolation of y in string
puts "I also said: '#{y}'."

# Assigning boolean false to variable hilarious
